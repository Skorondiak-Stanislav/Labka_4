"""
Знайти значення  .
y = 1,якщо2x^2-3=0,
y = 2x,якщо2x^2-3>0,
y = x,якщо2x^2-3<0.
"""
# позначення
'''
x - змінна float
y - результат
'''
# введення
x = int(input('Введіть значення х :'))
# перевірка
if 2*(x**2)-3 < 0:
    print('y={0}'.format(x))
elif 2*(x**2)-3 == 0:
    print('y={0}'.format(1))
elif 2*(x**2)-3 > 0:
    print('y={0}'.format(2*x))
